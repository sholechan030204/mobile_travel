class TravelPlace {
  final String id;
  final String name;
  final String description;
  final String imageUrl;
  final double price;
  final int duration;
  final int maxGroupSize;

  TravelPlace({
    required this.id,
    required this.name,
    required this.description,
    required this.imageUrl,
    required this.price,
    required this.duration,
    required this.maxGroupSize,
  });
}
